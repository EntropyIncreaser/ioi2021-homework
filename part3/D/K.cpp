template <class T>
istream& operator>>(istream& is, vector<T>& v) {
  for (T& x : v)
    is >> x;
  return is;
}

template <class T>
ostream& operator<<(ostream& os, const vector<T>& v) {
  if (!v.empty()) {
    os << v.front();
    for (int i = 1; i < v.size(); ++i)
      os << ' ' << v[i];
  }
  return os;
}

const int N = 100010, S = 15;

char str[S][N];
int fail[N];
pair<vector<int>, int> b[N];

int main() {
#ifdef ELEGIA
  freopen("test.in", "r", stdin);
  int nol_cl = clock();
#endif
  ios::sync_with_stdio(false);
  cin.tie(nullptr);

  int n, s;
  cin >> n >> s;
  for (int i = 1; i <= s; ++i) {
    cin >> (str[i] + 1);
    int len = strlen(str[i] + 1);
    memset(fail, 0, sizeof(fail));
    int k = 0;
    for (int j = 2; j <= len; ++j) {
      while (k && str[i][j] != str[i][k + 1]) {
        k = fail[k];
      }
      if (str[i][j] == str[i][k + 1]) {
        ++k;
      }
      fail[j] = k;
    }
    b[i].first.push_back(len);
    int x = fail[len];
    while (x && len * 2 - x <= n) {
      b[i].first.push_back(x);
      x = fail[x];
    }
  }
  for (int i = 1; i <= s; ++i) {
    b[i].second = i;
  }
  sort(b + 1, b + s + 1, [&](const auto& p, const auto& q) {
    return p.first < q.first;
  });
  for (int i = 1; i <= s; ++i) {
    cout << (str[b[i].second] + 1) << '\n';
  }

#ifdef ELEGIA
  LOG("Time: %dms\n", int ((clock()
          -nol_cl) / (double)CLOCKS_PER_SEC * 1000));
#endif
  return 0;
}
